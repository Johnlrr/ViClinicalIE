from __future__ import annotations

import argparse
import sys
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parents[1]
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from src.config import load_config
from src.io_utils import write_json, write_text
from src.ner.dataset_builder import build_ner_dataset, write_ner_dataset, write_ner_dataset_report


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Build Phase 14 NER dev-gold and weak-label datasets without training a model.")
    parser.add_argument("--config", default="configs/default.yaml")
    parser.add_argument("--gold-input-dir", default=None)
    parser.add_argument("--gold-dir", default=None)
    parser.add_argument("--weak-input-dir", default=None)
    parser.add_argument("--weak-pred-dir", default="outputs/predictions/submission_phase9/output")
    parser.add_argument("--dev-output", default=None)
    parser.add_argument("--weak-output", default=None)
    parser.add_argument("--report-dir", default=None)
    parser.add_argument("--weak-types", nargs="*", default=None)
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    config = load_config(args.config)
    ner_cfg = dict(config.raw.get("ner", {}))
    dataset_cfg = dict(ner_cfg.get("dataset", {}))

    gold_input_dir = _resolve_arg(config.project_root, args.gold_input_dir) or config.path("golden_input_dir")
    gold_dir = _resolve_arg(config.project_root, args.gold_dir) or config.path("golden_gold_dir")
    weak_input_dir = _resolve_arg(config.project_root, args.weak_input_dir) or config.path("raw_input_dir")
    weak_pred_dir = _resolve_arg(config.project_root, args.weak_pred_dir)
    dev_output = _resolve_arg(config.project_root, args.dev_output) or _resolve(config.project_root, dataset_cfg.get("dev_gold_path", "data_train/ner/dev_gold.jsonl"))
    weak_output = _resolve_arg(config.project_root, args.weak_output) or _resolve(config.project_root, dataset_cfg.get("train_weak_path", "data_train/ner/train_weak.jsonl"))
    report_dir = _resolve_arg(config.project_root, args.report_dir) or _resolve(config.project_root, dataset_cfg.get("report_dir", "outputs/reports/phase14_ner_dataset"))
    weak_types = set(args.weak_types or dataset_cfg.get("weak_types", ["TRIỆU_CHỨNG", "CHẨN_ĐOÁN"]))

    dev_examples, dev_summary = build_ner_dataset(
        input_dir=gold_input_dir,
        annotation_dir=gold_dir,
        source_name="gold",
        output_path=dev_output,
        label_types=None,
    )
    write_ner_dataset(dev_examples, dev_output)
    write_ner_dataset_report(dev_summary, report_dir)

    weak_examples, weak_summary = build_ner_dataset(
        input_dir=weak_input_dir,
        annotation_dir=weak_pred_dir,
        source_name="phase9_weak",
        output_path=weak_output,
        label_types=weak_types,
    )
    write_ner_dataset(weak_examples, weak_output)
    write_ner_dataset_report(weak_summary, report_dir)

    combined = {
        "dev_gold": dev_summary.to_dict(),
        "train_weak": weak_summary.to_dict(),
        "weak_types": sorted(weak_types),
        "note": "Phase 14 infrastructure only: datasets built, no model training performed.",
    }
    write_json(report_dir / "summary.json", combined)
    labels = ["O"]
    for entity_type in config.raw.get("ner", {}).get("labels", []):
        labels.extend([f"B-{entity_type}", f"I-{entity_type}"])
    write_json(Path(dev_output).parent / "label_map.json", {"label2id": {label: idx for idx, label in enumerate(labels)}, "id2label": {str(idx): label for idx, label in enumerate(labels)}})
    write_text(
        Path(dev_output).parent / "README.md",
        "# Phase 14 NER datasets\n\n"
        "This directory is generated by `scripts/build_ner_dataset.py`.\n\n"
        "- `dev_gold.jsonl`: 20 golden files converted to NER examples.\n"
        "- `train_weak.jsonl`: weak labels from Phase 9 100-file predictions.\n"
        "- No model is trained in Phase 14 infrastructure mode.\n",
    )

    print("Phase 14 NER dataset build completed.")
    print(f"dev_gold_files: {dev_summary.file_count}")
    print(f"dev_gold_entities: {dev_summary.entity_count}")
    print(f"dev_gold_offset_errors: {dev_summary.offset_error_count}")
    print(f"weak_files: {weak_summary.file_count}")
    print(f"weak_entities: {weak_summary.entity_count}")
    print(f"weak_offset_errors: {weak_summary.offset_error_count}")
    print(f"weak_overlap_conflicts: {weak_summary.overlap_conflict_count}")
    print(f"report_dir: {report_dir}")


def _path(value: str | None) -> Path | None:
    return Path(value) if value else None


def _resolve_arg(root: Path, value: str | None) -> Path | None:
    path = _path(value)
    if path is None:
        return None
    return path if path.is_absolute() else root / path


def _resolve(root: Path, value: str | Path) -> Path:
    path = Path(value)
    return path if path.is_absolute() else root / path


if __name__ == "__main__":
    main()
